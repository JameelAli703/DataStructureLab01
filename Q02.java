package DSA_LAB_01;

public class Q02 {
    public static void main(String[] args) {
        int a = 2;
        int b = 3;
        int c;
        System.out.println("Before Swapping");
        System.out.println(a+","+b);
        c = a;
        a = b;
        b = c;
        System.out.println("After Swapping");
        System.out.println(a+","+b);
    }
}
